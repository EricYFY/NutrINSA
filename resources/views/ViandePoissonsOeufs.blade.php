    	&nbsp &nbsp &nbsp &nbsp Le PNNS (Plan National Nutrition Santé), mis en place par l’état avec pour objectif l’amélioration de l’état de santé de l’ensemble de la population française, recommande de consommer un aliment du groupe « Viandes, poissons, œufs » une à deux fois par jour.
	</br>
    	&nbsp &nbsp &nbsp &nbsp La fréquence recommandée de viande fraîche (hors plats cuisinés à base de viande) est de 4 repas sur 20 repas consécutifs.
	</br>
    	&nbsp &nbsp &nbsp &nbsp La portion journalière recommandée pour un adulte est d’environ 100-150g, à répartir sur un ou deux repas.
	</br>
    	&nbsp &nbsp &nbsp &nbsp En France, la consommation moyenne de viande de boucherie (Bœuf, Porc, Veau, Agneau et Viande de Cheval) s’établit à 46g/jour. Soit une consommation moyenne d’environ 320g de viande fraîche par semaine (environ 370g en rajoutant la viande des plats cuisinés). Ce qui équivaut à 3-4 portions par semaine, en deça des recommandations du PNNS.
	</br>
    	&nbsp &nbsp &nbsp &nbsp Une majorité de français (56%) sont de petits consommateurs de viande (moins de 45g/j) : ce qui peut entraîner des difficultés pour couvrir les apports recommandés en protéines de bonne qualité, en vitamines ou en fer, notamment pour les enfants, femmes et séniors qui sont sujets à des carences en certains nutriments.
